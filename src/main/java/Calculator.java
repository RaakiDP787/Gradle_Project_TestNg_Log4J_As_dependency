public class Calculator {
    public static int additionOfTwoNumbers(int number1 , int number2){
        return number1 + number2;
    }
    public static int subtractionOfTwoNumbers(int number1 , int number2){
        return number1 - number2;
    }
    public static int multiplyOfTwoNumbers(int number1 , int number2){
        return number1 * number2;
    }
    public static int divisionOfTwoNumbers(int number1 , int number2){
        return number1 / number2;
    }
}
